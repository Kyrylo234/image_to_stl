import gradio as gr
from PIL import Image, ImageFilter
import numpy as np
import struct
import trimesh
import tempfile

def generate_triangles_vectorized(data, scale):
    h, w = data.shape
    h1 = data[:-1, :-1] * scale
    h2 = data[:-1, 1:]  * scale
    h3 = data[1:, :-1]  * scale
    h4 = data[1:, 1:]   * scale

    y = np.arange(h - 1, dtype=np.float32) * scale
    x = np.arange(w - 1, dtype=np.float32) * scale
    X0, Y0 = np.meshgrid(x, y)
    X1 = X0 + scale
    Y1 = Y0 + scale

    return X0, Y0, X1, Y1, h1, h2, h3, h4


def stream_stl(triangle_arrays, outfile):
    X0, Y0, X1, Y1, h1, h2, h3, h4 = triangle_arrays
    h_blocks, w_blocks = h1.shape

    with open(outfile, "wb") as f:
        header = b"Generated by image_to_stl"
        f.write(header.ljust(80, b" "))

        n_triangles = h_blocks * w_blocks * 2
        f.write(struct.pack("<I", n_triangles))

        for y in range(h_blocks):
            for x in range(w_blocks):
                v1 = (X0[y, x], Y0[y, x], h1[y, x])
                v2 = (X1[y, x], Y0[y, x], h2[y, x])
                v3 = (X0[y, x], Y1[y, x], h3[y, x])
                v4 = (X1[y, x], Y1[y, x], h4[y, x])

                # Triangle 1
                f.write(struct.pack("<3f", 0,0,0))
                f.write(struct.pack("<3f", *v1))
                f.write(struct.pack("<3f", *v2))
                f.write(struct.pack("<3f", *v3))
                f.write(struct.pack("<H", 0))

                # Triangle 2
                f.write(struct.pack("<3f", 0,0,0))
                f.write(struct.pack("<3f", *v2))
                f.write(struct.pack("<3f", *v4))
                f.write(struct.pack("<3f", *v3))
                f.write(struct.pack("<H", 0))


def image_to_stl(file, max_height, blur_radius):

    # Load image
    img = Image.open(file.name)

    if img.mode in ("RGBA", "LA"):
        bg = Image.new("RGB", img.size, (255, 255, 255))
        bg.paste(img, mask=img.split()[-1])
        img = bg

    img = img.convert("L").filter(ImageFilter.GaussianBlur(blur_radius))

    data = (1 - (np.array(img) / 255.0)) * max_height
    data = np.flipud(data)

    scale = 0.01
    triangles = generate_triangles_vectorized(data, scale)

    # --- Create temporary filenames for STL and GLB ---
    stl_tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".stl")
    glb_tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".glb")

    stl_path = stl_tmp.name
    glb_path = glb_tmp.name

    stl_tmp.close()
    glb_tmp.close()

    # Write STL to temp file
    stream_stl(triangles, stl_path)

    # Convert STL → GLB
    mesh = trimesh.load(stl_path)


    # Add simple PBR material so it isn't dark
    material = trimesh.visual.material.PBRMaterial(
        baseColorFactor=[1.0, 1.0, 1.0, 1.0],   # bright white
        metallicFactor=0.0,                    # non-metal
        roughnessFactor=0.8                    # diffuse lighting
    )

    # Apply material to mesh
    mesh.visual = trimesh.visual.TextureVisuals(material=material)


    mesh.export(glb_path)

    # Return temp file paths for Gradio
    return glb_path, stl_path


# Gradio interface
iface = gr.Interface(
    fn=image_to_stl,
    inputs=[
        gr.File(file_types=[".png", ".jpg", ".jpeg"]),
        gr.Slider(0, 50, value=1, label="Max Height"),
        gr.Slider(0, 10, value=0, step=1, label="Blur Radius")
    ],
    outputs=[
        gr.Model3D(label="3D Preview (GLB)"),
        gr.File(label="Download STL")
    ],
    title="Image to STL Generator",
    description="Upload an image, adjust height/blur, preview in 3D, and download the STL file."
)

iface.launch()